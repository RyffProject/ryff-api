            <p>
                Ryff was conceived at LA Hacks 2014 by Chris Laganiere and Robert 
                Fotino, two first-year computer science students at UCLA.
            </p>
            
            <p>
                Ryff is an app to help musicians find each other. You can create an artist 
                profile, add your musical interests, and connect with other local 
                musicians looking to play. Ryff allows you to record and post 
                samples of your music without leaving the app, so you can show off your talents
                to people in the area. Do your friends have a band, but nobody 
                wants to play drums? Start a group on Ryff and search for nearby 
                artists who play drums. Trying to find a guitarist who's into a 
                specific genre? You can do that, too.
            </p>

            <p>
                Ryff allows you to connect with the people that you want to be 
                connected with. You can browse nearby artists and musical groups 
                with similar tastes, hear how they sound, and either add them to 
                your favorites or skip them and move on to the next. If you like 
                the music of a particular artist, you should message them and meet up. 
                Here at Ryff it's our belief that you should <em>Never Jam Alone</em>.
            </p>
            
            <p>
                If you have an iPhone, Ryff is coming soon to an app store near you. 
